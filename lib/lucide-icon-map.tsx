/**
 * Render a Lucide icon by its PascalCase name string.
 *
 * Falls back to CircleDot if the name isn't found in the map.
 */
export function LucideIconByName({
  name,
  size = 20,
  color = '#FFFFFF',
  strokeWidth = 2,
}: {
  name: string;
  size?: number;
  color?: string;
  strokeWidth?: number;
}) {
  const Icon = ICON_MAP[name] ?? CircleDot;
  return <Icon size={size} color={color} strokeWidth={strokeWidth} />;
}

/** Get the raw component for a Lucide icon name (for advanced usage). */
export function getLucideIcon(name: string): LucideIcon {
  return ICON_MAP[name] ?? CircleDot;
}
